#include <iostream>
using namespace std;

int main()
{
    int num;
    cout << "Digues un numero: ";
    cin >> num;
    if (num < 1)
    {
        cout << "Hello World!\n";
    }
    if (num == 1)
    {
        cout << "Recorcholis\n";
    }
    if (num > 1)
    {
        cout << "Bye World!\n";
    }
    if (true)
    {
        cout << "Pues es verdad\n";
    }
    if (false)
    {
        cout << "Nunca se me vera :C\n";
    }
    if (num == 24)
    {
        cout << "Es mi numero favorito :D\n";
    }
    else
    {
        cout << "Ese no es mi numero favorito :C\n";
    }
    return 0;
}

